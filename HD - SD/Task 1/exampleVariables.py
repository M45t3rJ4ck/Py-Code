#========= Variables ===========
# We can imagine that a variable is a place-holder which holds certain things on-demand. 
#We use variables to store data in a program.

num = 2  
num = 3  
# We assigned the number 2 to the variable 'num'
# Then we overwrite num with the value of the 3 and 2 is automatically 'forgotten'

#When coding in Python, you can use call variables anything you want, but try using descriptive variable names.

myName = "Tom"      
variableOne = "Tom" 

# myName is a good variable name
# variableOne is not a good variable name, doesn't give a description of what would be stored inside it
# Variables can store different Data Types.

numTwo = 3 
nameTwo = "John" 
numThree = 0.5 

# The variable numTwo stores an integer value
# The variable nameTwo stores and String value
# The variable numThree stores a double, or real numbered, value

# We can output the value of variables as follows:

print(numTwo )
print(nameTwo)
print(numThree) 

# Run this program and take a look at the output. The three lines above generated that output.
# They printed out the VALUES of each variable.

