# ================== A Quick Note on Variables ==================
# In a program, variables act  as a kind of “storage location” for data 
# They are a way of naming or labelling information, so that we can refer to that particular piece of information later on in the program.
# For example, say you what to store the age of the user so that the program can use it later.
# You can store the user's age in a variable called “age”.
# Now every time you need the user's age, you can use the variable “age” to reference it.
# As you can see variables are very useful when you need to use and keep track of multiple pieces of information in your program.
# This is just a quick introduction to variables, you will get a more in depth explanation later on in this course.


